#include "diskio.h"		/* FatFs lower layer API */
#include "bsp_spi_flash.h"

/* Definitions of physical drive number for each drive */
#define SPI_FLASH		0	/* Example: Map ATA harddisk to physical drive 0 */


/*-----------------------------------------------------------------------*/
/* Get Drive Status                                                      */
/*-----------------------------------------------------------------------*/

DSTATUS disk_status (
	BYTE pdrv		/* Physical drive nmuber to identify the drive */
)
{
    switch (pdrv) {
	case SPI_FLASH :
		return FATFS_FLASH_SPI_disk_status();

	}

	return STA_NOINIT;
}



/*-----------------------------------------------------------------------*/
/* Inidialize a Drive                                                    */
/*-----------------------------------------------------------------------*/

DSTATUS disk_initialize (
	BYTE pdrv				/* Physical drive nmuber to identify the drive */
)
{
	switch (pdrv) {
	case SPI_FLASH :
		return FATFS_FLASH_SPI_disk_initialize();

	}

	return STA_NOINIT;
}



/*-----------------------------------------------------------------------*/
/* Read Sector(s)                                                        */
/*-----------------------------------------------------------------------*/

DRESULT disk_read (
	BYTE pdrv,		/* Physical drive nmuber to identify the drive */
	BYTE *buff,		/* Data buffer to store read data */
	DWORD sector,	/* Sector address in LBA */
	UINT count		/* Number of sectors to read */
)
{
	switch (pdrv) {
	case SPI_FLASH :
		return FATFS_FLASH_SPI_disk_read(buff, sector, count);

	}

	return RES_PARERR;
}



/*-----------------------------------------------------------------------*/
/* Write Sector(s)                                                       */
/*-----------------------------------------------------------------------*/

#if _USE_WRITE
DRESULT disk_write (
	BYTE pdrv,			/* Physical drive nmuber to identify the drive */
	const BYTE *buff,	/* Data to be written */
	DWORD sector,		/* Sector address in LBA */
	UINT count			/* Number of sectors to write */
)
{
    if (!count) {
		return RES_PARERR;		/* Check parameter */
	}
	switch (pdrv) {
        case SPI_FLASH :
            return FATFS_FLASH_SPI_disk_write((BYTE *)buff, sector, count);

	}

	return RES_PARERR;
}
#endif


/*-----------------------------------------------------------------------*/
/* Miscellaneous Functions                                               */
/*-----------------------------------------------------------------------*/

#if _USE_IOCTL
DRESULT disk_ioctl (
	BYTE pdrv,		/* Physical drive nmuber (0..) */
	BYTE cmd,		/* Control code */
	void *buff		/* Buffer to send/receive control data */
)
{
	switch (pdrv) {
        case SPI_FLASH :
            return FATFS_FLASH_SPI_disk_ioctl(cmd, (char *)buff);

	}

	return RES_PARERR;
}
#endif
